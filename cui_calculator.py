num1 = int(input('Enter First Input:'))
num2 = int(input("Enter second Input:"))
#Arithmetic Operation:Addition
print("Output of Addition is:".format(num1, num2),num1+num2)

#Arithmetic Operation:Subtraction
print("Output of Subtraction is:".format(num1, num2),num1-num2)

#Arithmetic Operation:Multiplication
print("Output of Muliplication is:".format(num1, num2),num1*num2)

#Arithmetic Operation:Division
print("Output of Division is:".format(num1, num2),int(num1/num2))

